    logger.info(f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {call.from_user.id} —Å–¥–µ–ª–∞–ª –∑–∞–ø—Ä–æ—Å –ø–æ –∞–∫—Ü–∏—è–º.")
